                  <p>ดร.วรรณี ฉินศิริกุล ผู้อำนวยการศูนย์นาโนเทคโนโลยีแห่งชาติ สำนักงานพัฒนาวิทยาศาสตร์และเทคโนโลยีแห่งชาติ (สวทช.) กล่าวในการเปิดงานพิธีลงนามในบันทึกความร่วมมือ “โครงการพัฒนาฐานข้อมูลสมุนไพรเพื่อการใช้ประโยชน์ทางเครื่องสำอางและผลิตภัณฑ์สมุนไพรสำหรับใช้ภายนอก” ว่า สวทช. เป็นหน่วยงานภายใต้กระทรวงการอุดมศึกษา วิทยาศาสตร์ วิจัยและนวัตกรรม (อว.) มีภารกิจที่สำคัญด้านการพัฒนาวิจัย ซึ่งรวมถึงการวิจัย คิดค้นผลงานที่มีการนำพืชสมุนไพรมาใช้เป็นวัตถุดิบตั้งต้น โดยใช้องค์ความรู้ เทคโนโลยีและนวัตกรรมขั้นสูงมาใช้ ซึ่งดำเนินการโดยศูนย์วิจัยแห่งชาติ ภายใต้ สวทช. อาทิ ศูนย์นาโนเทคโนโลยีแห่งชาติ รวมไปถึงการกำหนดกลุ่มเทคโนโลยีเป้าหมาย (Technology Development Group: TDG) ด้านเวชสำอาง (TDG: Cosmeceuticals) ซึ่งเป็นโปรแกรมวิจัยที่มุ่งเน้นการพัฒนาผลิตภัณฑ์เวชสำอางที่พัฒนาเพื่อเพิ่มประสิทธิภาพของผลิตภัณฑ์ รวมถึงการพัฒนาเทคโนโลยีการสกัดแบบ Green extraction หากแต่ความต้องการสำคัญของการพัฒนาผลิตภัณฑ์คือ แหล่งของข้อมูลที่มีความน่าเชื่อถือเกี่ยวกับสมุนไพรที่สามารถนำมาใช้ประโยชน์สำหรับการพัฒนาผลิตภัณฑ์เครื่องสำอางและผลิตภัณฑ์สมุนไพรสำหรับใช้ภายนอก ซึ่งประเทศไทยมีเพียง 1 ฐานข้อมูล คือ ฐานข้อมูลสมุนไพรไทยที่ใช้ทางเครื่องสําอาง (Thai Medicinal Plants for Cosmetics Database; TMPCD) ของกลุ่มควบคุมเครื่องสำอาง สำนักควบคุมเครื่องสำอางและวัตถุอันตราย สำนักงานคณะกรรมการอาหารและยา (อย.)</p>
